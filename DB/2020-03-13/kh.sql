--1. BASIC LOOP : 조건 없이 무한 반복
DECLARE
    I NUMBER := 1;
BEGIN
    LOOP        --반복 시작
        DBMS_OUTPUT.PUT_LINE(I);
        I := I + 1;
        IF I > 5 
            THEN EXIT;
        END IF;
    END LOOP;   --반복 끝지점
END;
/

--2. FOR LOOP
DECLARE
BEGIN
    FOR I IN REVERSE 1..5 LOOP
        DBMS_OUTPUT.PUT_LINE(I);
    END LOOP;
END;
/

